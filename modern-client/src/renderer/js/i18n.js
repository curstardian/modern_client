  function currentLang() {
    const settings = window.Store ? window.Store.get().settings : null;
    const lang = settings && settings.language;
    return STRINGS[lang] ? lang : 'ko';
  }

  function format(str, params) {
    if (!params) return str;
    return Object.keys(params).reduce(
      (acc, key) => acc.split(`{${key}}`).join(params[key]),
      str,
    );
  }

  function t(key, params) {
    const lang = currentLang();
    const raw = (STRINGS[lang] && STRINGS[lang][key]) || STRINGS.ko[key] || key;
    return format(raw, params);
  }

  function applyStaticLabels(root) {
    (root || document).querySelectorAll('[data-i18n]').forEach((el) => {
      el.textContent = t(el.dataset.i18n);
    });
  }

  return { t, applyStaticLabels, currentLang };
